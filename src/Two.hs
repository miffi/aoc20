type Input = [Entry]
data Entry = Entry Int Int Char String
  deriving Show

main :: IO ()
main = print $ answer2 input

answer1 :: Input -> Int
answer1 = length . filter (==True) . map check
  where
    check :: Entry -> Bool
    check (Entry start end char string) = let x = length . filter (==char) $ string
                                           in x >= start && x <= end

answer2 :: Input -> Int
answer2 = length . filter (==True) . map check
  where
    check (Entry x y char string) = let len = length string
                                        xTest = if (x-1) > len then False else string !! (x - 1) == char
                                        yTest = if (y-1) > len then False else string !! (y - 1) == char
                                      in xTest /= yTest

